package org.erlymon.litvak.monitor.view.fragment;

import android.os.Build;
import android.support.v4.app.DialogFragment;

/**
 * Created by Sergey Penkovsky <sergey.penkovsky@gmail.com> on 23.10.15.
 */
public class BaseDialogFragment extends DialogFragment {

    protected boolean isBrokenSamsungDevice() {
        return (Build.MANUFACTURER.equalsIgnoreCase("samsung")
                && isBetweenAndroidVersions(
                Build.VERSION_CODES.LOLLIPOP,
                Build.VERSION_CODES.LOLLIPOP_MR1));
    }

    protected boolean isBetweenAndroidVersions(int min, int max) {
        return Build.VERSION.SDK_INT >= min && Build.VERSION.SDK_INT <= max;
    }
}
